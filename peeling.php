 	  <h1> When to Dive and When to Peel </h1>
	  
	  <h3> This is a CRUCIAL skill in League of Legends</h3>
	  
	  <p>Knowing when to peel and when to dive an enemy carry is one of the most important and fundamental things
	  to learn in League of Legends. <strong>Peeling is not just the support's job, in fact some are very poor at peeling
	  and will likely require help.</strong> If you do not master this skill you will lose a considerable amount of games
	  because of it. A common mistake people make is to dive an enemy carry at all costs and don't even give
	  consideration to peeling for their own carry (or carries.) This is an easy skill to learn and master but it's something
	  that people tend to overlook and don't think about mastering. </p>
	  
	  <h3> When to peel for your carries </h3>
	  
	  <p>Generally you want to peel when your carries out damage the enemy carries. This seems fairly obvious but
	  a lot of people overlook it. It's also hard to figure out which carries do the most damage since sometimes
	  you can have an ADC with no kills out damaging an enemy carry with 4+ kills simply because their champion is
	  either stronger at that point in the game (more on that later) or has been farming for a large portion of the game
	  and actually has a significant gold advantage on the enemy carry.
	  
	  <br>
	  <br>
	  
	  This can also be tricky because some team comps are not designed well for peeling. For example, peeling as a Zed is
	  nowhere near as effective as diving simply because he is an assassin and his kit is designed for diving. This really
	  goes on a case by case basis, just be sure to consider your team comp before you decide whether you should peel or dive
	  the enemy carries. This will get easier to determine the more games you play, but these are good guidelines to go by when you
	  are unsure of what to do. Simply determine which team's carries do more damage and decide from there.</p>
	  
	  <h3> CC is great for peeling -- but not the only way </h3>
	  
	  <p>A lot of people don't even give consideration to peeling for their carries if they're playing a champion such as
	  Shyvana who simply has no CC and cannot lock down an enemy assassin. While this does make peeling more difficult and
	  that should be considered when deciding which to do, there are plenty of items that can be bought to slow an enemy down
	  and stop them from reaching your carries (items such as Rylai's Crystal Scepter or Frozen Mallet can slow enemies down
	  and make peeling easy. Plus, the most effective way to peel an enemy off your carries is to kill them!</p>
	  
	  <h3> When to dive the enemy carries </h3>
	  
	  <p>Since you want to peel for your carries if they out-damage the enemy carries, you want to dive the enemies carries if they out damage yours.
	  This is highly situational as well though because some champions simply cannot deal enough damage or can be kited easily in teamfights
	  making them effectively useless should they decide to dive the enemy carries. <strong>Tanks should never dive alone because they
	  don't have the damage output to kill the enemy carries.</strong> Tanks are useful however in tandem with an assassin because they excel in
	  locking down an enemy carry for a fellow teammate to kill them.
	  
	  
	  <br>
	  <br>
	  
	  As their name implies, assassins are generally the best at killing enemy carries but sometimes will struggle in the presence of
	  enemy tanks. If you have a fed assassin on your team you should look to assist them if possible (this is assuming of course that
	  the enemy carries are worthy of diving.) A great tactic for bruisers/tanks assisting their assassins is to lock-down the enemy carry
	  that the assassin is targeting and then try to navigate your way back to your own backline and help peel as fast as possible. In most
	  cases the support can peel for a short amount of time while you lock down the enemy carries and this will give you a small window to 
	  both dive and peel.</p>
	  
	  <h3> The more you play, the easier it gets </h3>
	  
	  <p>Overall, deciding when to peel and when to dive is something that you will be able to decide easier the more you play and think about the situation
	  before a fight ensues. Deciding between peeling and diving can determine which teams wins a game and it will come easier the more you are familiar
	  with the different champions and their abilities. Based on whether you win a fight you can sometimes determine if you made the correct choice. Also pay
	  attention if one of your carries is complaining about lack of peel, while some may simply not understand why your team is diving instead of protecting them,
	  they may also be great tools to determine whether your team is peeling them enough and it can help you decide what to do.</p> 
	  
