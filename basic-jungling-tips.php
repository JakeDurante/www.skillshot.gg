	  <h3> When do I gank? </h3>
	  
	  <p> Sometimes the hardest part of jungling is determining when to gank for a teammate. Generally there a few indicators of
	  when it's a good time to gank. If you see an opponent pushed very far up, then it is almost always a good time to gank (assuming
	  they aren't super ahead because then they can 1v2 you.) Another great time to look for a gank is when the enemy is low on hp or mana
	  and cannot escape quickly (e.g. leblanc with her 2 dashes and flash can probably escape.) There are plenty of other times when it's
	  appropriate to gank an enemy and you will start to notice intuitively when it's a good time to gank as you keep playing the role. </p>
	  
	  <h3> Objectives </h3>
	  
	  <p> Most information about objectives and when to take them has been convered in the <a href="objective-control.php"> objectives article.</a> 
	  However there is some jungler specific information which is important to know. Some junglers, like warwick, can solo the dragon very easily and
	  very early. While this may not always be a good strategy as it puts you slightly behind it can benefit your team greatly (especially if the dragon
	  is a higher priority dragon, see the article on <a href="dragons.php">dragons.</a>) If you do decide to solo the dragon early, be sure to take a pink ward and make sure it isn't warded and be prepared to run if you
	  are discovered taking the dragon.
	  
	  <h3> Practice makes perfect </h3>
	  
	  <p>Jungling is a very different role from most of the others and requires a lot of thinking and strategizing both in-game and before
	  the game even starts. It will take a lot of practice to truly understand when to gank, when to farm, when to take dragon, when to take baron,
	  etc. But it will start to become something you don't even think about once you get better at it so don't give up! Jungle also has huge carry potential,
	  if you are able to outsmart the enemy team and successfully make decisions while in the jungle, you can easily be the catalyst for your team's success
	  and win the game outright.</p>
	  
