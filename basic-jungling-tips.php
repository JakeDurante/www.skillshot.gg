 	  <h1> General Jungling Advice </h1>
	  
	  <h3> What are my responsibilities? </h3>
	  
	  <p>Jungling is a very cerebral role. It requires a lot of mental effort because you have so much to
	  keep track of and a lot of planning and strategy. If you want to be successful as a jungler you <strong>
	  must have good map awareness!</strong> Junglers are required to keep a constant eye on all 3 lanes, their buffs,
	  dragon/baron/rift, and the enemy jungler at all times. Since you're fighting jungle camps that are static and
	  (hopefully) won't kill you, you can constantly focus your attention on the lanes.
	  
	  <br>
	  <br>
	  Be sure to assist teammates in looking out for enemy ganks and warding to provide vision (scuttle crabs are great
	  for this because they give you gold too!) When playing jungle you should always strive to have all of your wards down and
	  to try to keep a pink ward down constantly. When you have wards down be sure to use them and ping your teammates of any
	  enemies show up to alert them because they may not notice. There's no reason not to ping and help your teammates especially in lower
	  elos where they might not have great map awareness yet. As the jungler you are the eyes and ears of your team since you
	  have nothing to focus on when you're farming.</p>
	  
	  <h3> Ganking </h3>
	  
	  <p><strong>Ganking as the jungler is one of your most important duties.</strong> The level of ganking that you should do
	  varies wildly depending on the game situation and the champions you and your team are playing. If you are playing
	  a gank heavy champion like Lee Sin or Elise then you should be focusing on ganking as much as possible and getting your
	  lanes ahead.
	  <br>
	  <br>
	  When the game starts you need to evaluate all three lanes and see which have the best potential for a successful gank.
	  (look at how your cc matches up or if you have a ton of burst) and consider which laners will snowball the hardest. This
	  will allow you to determine a priority on which to gank your lanes. This will help you refrain from wasting time ganking a
	  Rammus vs Maokai lane where it's unlikely a gank will work and even if it does, neither laner will snowball very hard. This
	  technique will allow you to focus on the lanes where ganks either come easy to due to a ton of cc or will let you focus on
	  lanes where the laner can take over the game if they get fed enough.
	  
	  <br>
	  <br>
	  
	  When planning a gank consider the odds of success and how much time it will take to pull it off. If you still think the
	  gank will work then begin to plan out your strategy. There are many routes to ganking an enemy, you can go through the lane
	  if it's top or bot and hide in the bushes which can be extremely effective or you can try to circle behind the enemy or take a
	  myriad of other paths to get to your target. <strong>If you successfully complete a gank and your laner is low on hp then
	  be sure to push the lane into the enemy tower</strong> (if you think you can take the tower, do it.) Sometimes laners will ping you off so you
	  "don't take their farm" or "stop taxing" but little do they realize that pushing the lane benefits them as well because it denies the enemy laner
	  gold and XP. The only times you might want to hesitate about pushing the lane is if your teammate is a very farm-heavy champion like Nasus
	  because by pushing the lane you deny him stacks, but in most cases pushing the wave to the tower is the right play. Being able to distinguish
	  situations where you should and shouldn't push the wave will simply come with time and experience, but this a good baseline for determining when and
	  when not to push the lane after the gank.
	  
