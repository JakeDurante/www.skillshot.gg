 	  <h1> General Support Advice </h1>
	  
	  <h3> Introduction to Support </h3>
	  
	  <p> The role of Support is often overlooked in League of Legends, it is actually one of the most important roles in the game and good supports can
	  easily carry a team. Support is generally the least popular role because of this misconception and because there is no tangible way of measuring
	  whether or not a support is a good player. Their K/D/A can be awful but they can still carry. The main job of the support is to keep their ADC safe
	  while providing vision and depending on the champion, provide catch potential for the rest of their team. Supports such as Blitzcrank and Thresh can completely
	  turn a game around with one good hook and supports such as Janna or Soraka can do the same thing by denying an enemy assassin or by healing crucial
	  members of their team and allowing them to win fights due to their immense amount of healing and/or CC. 
	  
	  
	  <br>
	  <br>
	  
	  Supports in laning phase can either be aggressive such as Nautilus or Alistar and try to CC the enemy ADC and secure a kill for their laner or they can
	  be defensive and try to simply win lane by keeping their ADC alive and allowing them to farm safely. Supports will generally buy one of several items designed
	  for them, (generally called "support items") which allow them to generate gold in ways other than CSing either allowing them to kill an enemy minion and give the
	  gold to themselves and a nearby champion, or allowing them to generate gold by hitting an enemy or enemy tower, or allow them to simply generate passive gold faster
	  than other champions. This allows them to be somewhat close to the gold totals of the rest of their team, although due to the fact that they don't CS they will still
	  generally be behind on XP and gold.</p>	
	  
	  <h3> Laning phase </h3>
	  
	  <p>Laning phase is the bread and butter of the support. Their main goal at this stage of the game is simply to get their ADC ahead and keep them alive. This means
	  they need to be able to protect their ADC from ganks and CC in lane as well as ward for incoming dangers and be alert for enemy teleports and roams. Supports in
	  most cases should invest in a Sightstone which allows them to ward while freeing up their trinket slot for a sweeper. They also should be constantly buying pink
	  wards in order to secure even more vision for their team as well as deny enemy vision (although everyone on their team should be doing this.) The support should generally
	  not CS because it is important to get the ADC as much gold as possible although it is fine to do so when the ADC is at base. 
	  
	  <br>
	  <br>
	  
	  Supports should be very aware of what is happening around the map and should assist in pinging dangers out to teammates. Supports and junglers are generally the
	  strategists of the early game due to the low amount of micro skill required in the early stages of the game. Because of this you should always look for opportunities to roam
	  and help other lanes (assuming you're a support that can roam effectively.) This will allow you to help other lanes get ahead which is crucial to helping your team win the
	  game. Just keep in mind that when you roam you are leaving your ADC alone in lane which means they may get killed by the enemy bot lane.</p>
	 
	  <h3> Mid/Late game </h3>
	  
	  <p>Mid and late game the support's job is fairly simple. Your main priority is to keep your ADC (and any other carry) safe from the enemy team as they will try to dive them and kill them. Since
	  a lot of supports are squishy you also may be a good target to catch out so keep your positioning in mind as well. Depending on the champion that you are playing, you might also
	  be tasked with trying to catch and enemy out of position. Great champions for this are Blitzcrank, Thresh, Morgana, and Alistar. Catching an enemy out of position is huge because
	  it can allow the rest of your team to clean up the other 4 champions. You will also need to constantly keep up vision and buying pink wards whenever possible. Since you're the support and
	  you generally have less gold than other members of your team, you will have an opportunity to buy a pink ward for longer than other members of your team. Vision is very important at all
	  stages of the game but even more so in late game as one mistake can cost your team the game, and denying the enemy information is crucial to stopping them from taking advantage of any mistakes
	  made by your team.</p>
	  
