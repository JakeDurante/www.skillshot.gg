 	  <h1> Overcoming Ranked Anxiety </h1>
	  
	  <h3> Why do we experience it? </h3>
	  
	  <p> As I've played league over the years, I've encountered a lot of ranked anxiety stemming from people being scared they'll
	  either demote to the division below them or not complete the series the worked so hard to get to. My advice is to just find
	  a time you think you're in a good mindset to play and grind out the series. The more you dwell on the pressure that it
	  brings, the less you'll want to play league and the slower you'll improve. At the end of the day the ranked divisions are simply
	  icons to give you an idea of where you stand, they don't matter as you can always get better.
	  
	  <br>
	  <br>
	  I think a lot of people experience ranked anxiety and most don't know what to do about it. If this is something that affects you
	  try to think of ranked as a means to become better not a one time thing. It takes many people thousands upon thousands of games to
	  get to diamond and that's ok, because they enjoy the ride while they get there. If you are like most people reading these articles
	  and you want to improve, you'll likely get to diamond much, much faster than those who simply play like zombies and don't really try
	  to get better.
	  
	  <h3> Some tools to overcoming ranked anxiety </h3>
	  
	  <p>A lot of people recommend using music to calm you down and help you get over ranked anxiety and it seems like a great tool. The music
	  that is generally recommended is calming music if you get really nervous or even some pump-up music to get you in the zone to go out and
	  dominate the game. Some people create a smurf account and play that because it doesn't matter what rank that account becomes and with less
	  pressure coming from not playing on their main they tend to play better. It's often also recommended if you experience promo-anxiety to
	  try to play your promos right after you hit 100LP as you will be coming off of a win and will be generating some momentum.
	  
	  <br>
	  <br>
	  
	  At the end of the day league is a game and it's meant for fun. Don't worry about your rank and just try to focus on improving because
	  that is what will get you to rank up, not trying to squeeze every bit of LP out of your current skill level.</p>
	  
	  
	  
