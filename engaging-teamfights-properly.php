 	  <h1> Engaging in Teamfights </h1>
	  
	  <h3> Why it's important to master </h3>
	  
	  <p>Engaging is a very important skill in League of Legends because it allows your team to have the initiative
	  in a fight. This does not mean that your team should be the one engaging in every teamfight because some comps
	  are better at it than others. Engaging properly can allow your team to delete an enemy carry leaving them at
	  a numbers disadvantage and making the fight significantly easier for your team to win.</p>
	  
	  <h3> Generally tanks/bruisers are the ones initiating </h3>
	  
	  <p>Not all teamfights in league start with a clean 5v5 face off where one player starts it. A lot of the time
	  they start by someone getting caught and teams then following up. However in the situation where you are the
	  one in charge of engaging you need to <strong>be careful that you don't dive too far into the enemy team to the point that
	  your team cannot follow up properly.</strong> This can flip the fight the other way and leave your team at a numbers disadvantage
	  and making them lose the fight in most cases.
	  
	  <br>
	  <br>
	  
	  In most cases it's the tanks or bruisers starting the fights because they are strong enough to survive the enemy burst
	  allowing for the rest of their team to follow up and finish the fight. Assassins can also start a fight but it's very risky
	  because they need to be able to kill or significantly damage a carry and then get out before being burst down. Some champs such
	  as Leblanc excel at this because she can burst a carry and then teleport out of the fight immediately while she waits for her cooldowns
	  to expire. However if you're an assassin it generally is a much better idea to wait for a tank/bruiser to start the fight and then
	  try to take down an enemy carry. Flanking is a great way of doing this but certain champions have no trouble closing the gap and
	  getting through the enemy peel (e.g. Zed and Talon.)</p>
	  
	  <h3> Comboing with a fellow teammate is a great way to start a fight </h3>
	  
	  <p>Some champions that have AOE crowd control can lockdown the enemy team into a teammates ultimate. For example, an Amumu ulting and
	  allowing Miss Fortune to melt the enemy team with her ult since they cannot get out. <strong>This is a fantastic way to end fights quickly and
	  decisively and there are numerous combos like this in league.</strong> Before you engage and try to pull this off, be sure that both champions are
	  in range and can ult successively as well as be sure that both of your ults are up and ready to go for the ensuing fight. A common mistake
	  people make is trying to force an engagement like this without both people ready to go and it ends in disaster for them and usually loses the
	  fight.
	  
	  <br>
	  <br>
	  
	  Overall, engaging is a great way to quickly and decisively end fights because it gives your team significant initiative over the enemy team allowing
	  you to deal damage before they can respond. If you are able to AOE damage to multiple enemies or pick off an enemy carry this can leave them
	  considerably weaker before they respond allowing the rest of your team to quickly clean up the stragglers. Be careful not to go too far as you will
	  simply be peeled and deleted before your team can help you effectively flipping the initiative in the other direction and giving the enemy team a
	  large advantage in the rest of the fight.</p>
	  
	  
