 	  <h1> Smite Stealing </h1>
	  
	  <h3> Timing is key </h3>
	  
	  <p> Stealing objectives as a jungler can completely turn a game. <strong>It can turn certain losses into easy victories and as
	  such is a key skill that all junglers should learn be come proficient at because it will save you a lot of LP over the course
	  of your league career.</strong> Smite stealing is all about timing as you'll need to be able to beat the enemy jungler to smiting the
	  objective. Certain jungle champions have advantages in smite stealing, especially those with the ability to gap close and/or do
	  % missing health damage or high burst damage as it will allow you to dictate when the objective is within smitable range. Champions
	  that excel at stealing include:</p>
	  
	  <ul>
	  <li> Lee Sin </li>
	  <li> Elise </li>
	  <li> Nunu </li>
	  <li> Kha'zix</li>
	  <li>Rengar (at 5 ferocity)</li>
	  </ul>
	  
	  <p> Anytime you have an XP advantage on your opponent, you have a great opportunity to smite steal as your smite will do more damage
	  which will allow you to easily out damage them and steal an objective. If your team is able to follow you and you have a champion capable of
	  high burst, you can try to simply kill the enemy jungler before they are able to smite the objective. Generally if they are in range of the objective
	  will panic smite so be ready for that and secure the objective immediately after. Killing the enemy jungler is the best way of being able to secure
	  an objective but also takes the longest and if you're not careful they can burst baron and smite it before you are able to finish killing them.<strong> Keep
	  in mind that smite can be used while CC'd (including silenced.)</strong></p>
	  
	  <h3> When to attempt a smite steal </h3>
	  
	  <p>Generally a smite steal is not a good idea as in most cases even if it succeeds it will leave your team a man down and potentially give the enemy
	  team the man advantage necessary to simply end it. Also a common occurrence in solo queue is that when you go to attempt a steal, your teammates will
	  try to follow for whatever reason and simply die alongside you so be careful that you don't bait your team into a bad decision such as that. Smite steals
	  are generally good if your team is capable of holding back the enemies whilst you respawn or can even follow up with you and kill the enemy team while their
	  weakened by the objective. (This applies more to baron since dragon doesn't really do too much damage.) <strong>It it almost never worth it to attempt to steal a dragon
	  unless it's elder (but even that sometimes isn't worth it -- it depends on the amount of burn the elder buff will do to your team.)</strong> It is also nearly impossible
	  to steal when you are on the wrong side of the map. If you have to walk into the pit as opposed to going over the wall to steal, your chances of success go down dramatically
	  since you can be knocked back or simply killed before you can reach the objective. So do consider when trying to steal is worth it since the odds of securing the objective are
	  definitely in your opponent's favor.</p>
	  
	  <h3> Try to ping back teammates trying to steal without smite or a similar ability </h3>
	  
	  <p>Sometimes people on your team will try to steal the objective when they don't have smite. <strong>This is a very bad idea as the enemy can simply jump over the wall and kill them and their
	  odds of success are extremely low anyways since they do not have smite.</strong> The only time this may work is when you have a Cho'Gath or maybe a Kalista (although it's unlikely she'll be able
	  to stack enough for rend to actually work) or champions with similar abilities since they can sometimes out-smite the enemy jungler. If you see an ally that has very little chance of stealing the
	  objective be sure to try to ping them back as it will likely end up badly for your team. Sometimes of course they will not listen and will die, if that happens try to play safe and not concede too
	  much ground to the enemy team following the death of your teammate.
	  
