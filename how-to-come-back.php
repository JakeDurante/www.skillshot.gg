 	  <h1> How to Pull Off a Comeback </h1>
	  
	  <h3> Don't Give up! </h3>
	  
	  <p>A lot of times in league, especially with the toxic nature of the community at times, players tend to give up
	  when they see that they are down by a bit in games. (Some give up after first blood, but that's a whole
	  different story -- please don't do that.) The thing is, especially in lower elos,<strong> games are thrown all
	  of the time.</strong> When you're down by a lot of gold, a common mistake is players will keep fighting the enemy team
	  that has the gold/kill advantage and then dig themselves into a deeper hole. The key in this situation is
	  to focus on farming up and becoming stronger. When doing this, be on the look out for positional mistakes made
	  by the enemy team. These mistakes may allow you a chance to take an objective or catch a few enemies unaware, slowly
	  bringing your team back.
	  
	  <br>
	  <br>
	  
	  A lot of times, kills don't tell the whole story. There are plenty of games that are lost where the team that loses has
	  a kill advantage. When losing, try to find opportunities to split push because this allows you to farm up as well as
	  pressure objectives. This forces the enemy team to respond or lose crucial objectives and allowing your team to come back.
	  When executing this strategy it is <strong> crucial to ward and make sure you don't overextend. </strong>You also need to be
	  aware of where the enemy team is or might be because if they see people trying to split bot lane and baron is up, they will likely
	  try to take it, so be careful and consider where they are at all times.</p>
	  
	  <h3> Gold leads don't matter at full build </h3>
	  
	  <p> This is one thing that a lot of people don't think about when behind. A lot of times a lead by the enemy team is exacerbated
	  due to the fact that the enemies have an item and XP advantage on your team. When you reach late game where everyone is at full build,
	  a lot of that advantage shrinks quickly. The enemy team's gold lead doesn't help them at all if they can't buy items so be sure to target
	  late game if your team is really far behind as it is a great way to come back from a losing game.
	  
	  <br>
	  <br>
	  
	  Generally in this situation, the next team to win a fight decisively/get a catch on the enemy will win the game so be careful not to
	  overextend or push out by yourself into unwarded areas. A good technique for catching an enemy unaware if you don't have good catch potential is to
	  stack up in a "death bush" and wait for an unsuspecting enemy to walk by and then ambush them. This creates a numbers advantage and can easily
	  lead to ending the game quickly afterwards -- just be sure you sweep the bush or else it can backfire and keep your whole team grouped up for enemy
	  CC and AOE abilities.
	  
	  <h3> Scaling and decision-making are key </h3>
	  
	  <p> A key for having a team capable of a comeback is having a team comp that scales well. This is certainly not a requirement
	  but it makes comebacks much easier because your team will be stronger later in the game than your opponents. Champions that scale
	  well vary and as you play it becomes easier to determine which champions are better in late game. Another component of good teams
	  for comebacks is CC. This allows you to lock down fed enemies and protect your carries/eliminate the enemies carries. General traits
	  of late-game comps include: </p>
	  
	  <ul>
	  <li> Multiple ADCs or a great late-game ADC (Twitch, Tristana, Vayne) </li>
	  <li> Champions with large amounts of CC (Amumu, Annie, Orianna) </li>
	  <li> Champions with good synergy (Jarvan IV + Rumble, Orianna + Rengar) </li>
	  <li> Extremely high damage mages or assassins (Talon, Viktor, Zed, Azir) </li>
	  <li> Supports with catch potential (Morgana, Thresh, Blitzcrank) </li>
	  </ul>
	  
	  <p>There are many more examples of this than what was above, those were simply some of the most notable. However, even without a team like those listed above,
	  a comeback is very possible. Those champions/combos really do make late game comebacks much easier than without them. <strong>Any little mistake that an
	  enemy team makes that is successfully exploited can lead to a come back.</strong> A good example is taking baron at an improper time and giving it away. These
	  kind of throws are fairly common and are a reason why you should never give up.</p> 
	  
