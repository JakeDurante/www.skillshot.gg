 	  <h1> What is Tilt and How do I Deal With it?  </h1>
	  
	  <h3> What is Tilt? </h3>
 
 	  <p>"Tilt is a poker term for a state of mental or emotional confusion or frustration in which a player adopts a less than optimal strategy,
	   usually resulting in the player becoming over-aggressive." (Wikipedia) It generally leads to more mistakes usual. This usually occurs alongside
	   frustrating events such as losing, being mocked by an opponent, missing out on a great opportunity, internet issues, toxic teammates, etc.<br>
	  
	  <br>Generally, especially for newer players of League of Legends, it is very difficult to control tilting. Sometimes it
	  occurs and you don't even notice it because it's subconscious. Players tend to deal with tilt in a few different ways including:<br></p>
	  
	  <ul>
	  <li> Playing a normal game </li>
	  <li> Watching league streamers/youtubers </li>
	  <li> Watching replays/thinking about mistakes </li>
	  <li><strong> Taking a Break!!! </strong></li>
	  </ul>
	  
	  <p> Usually the best way to relieve tilt and come back to league playing better than before is to simply take a break. When you're tilted it's
	  very hard to focus on the thing that put you into that state and not stay that way. Some people can handle this and calm down when they see their
	  mistakes but most people simply tilt themselves more or do not gain any real insight when looking at replays because all they see are mistakes
	  from teammates instead of their own because they are not in the right mindset to be looking at replays. The reason taking a break is usually
	  the best method to relieving tilt is because when you preoccupy your mind with something else (usually you want something that's not frustrating as well)
	   you think more about what you're doing than how you just lost your game or how toxic your teammate was. <br>
	  
	  <br> The reason tilt is so devastating to even the best of players is because <strong>League is largely about confidence. </strong> When you
	  are confident in your ability you tend to play better which is why people who win one or two games tend to be able to extend that streak for
	  even longer. The same thing goes in the opposite direction. Along with tilt, when you start to lose multiple games in a row you start to lose
	  confidence in your ability to play to the highest caliber and you can see the effects if you watch replays of those games. When your confidence
	  wanes, you start to miss more skillshots, become excessively aggressive/passive (depending on your style), exhibit toxicity, and generally make bad decisions.
	  
	  <h3> Confidence and its effects on performance </h3>
	  
	  Have you ever played a game with someone who just completely dominated? If you look up their match history, generally those people are on a long winning
	  streak and are playing so well because they have confidence in their abilities. Confidence is one of the most important attributes to playing well in league
	  and its effects can be readily observed. The players who play best are generally high on confidence and those who are stuck in bad losing streaks are low on
	  confidence. (whether they realize it or not) Sometimes a lack of confidence can be noticed and other times it cannot. It manifests itself in self-doubt and
	  passiveness as well as other traits depending on your personality.<br>
	  
	  <br> A subconscious lack of confidence can be remedied by playing games in which you can win more easily, such as duoing (in normals!!) with lower elo friends.
	   If that is not an option, look into playing normals until you win a few and notice the shift in play style (it's fairly easy to notice once you look for it.)
	   Once you notch up a few wins you're ready to head back to ranked and keep grinding.
	  
	  
	  
	  
