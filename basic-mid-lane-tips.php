 	  <h1> General Mid Lane Advice </h1>
	  
	  <h3 id="display"></h3>
	  
	  <h3> Introduction to mid lane </h3>
	  
	  <p>Mid lane is generally regarded as one of the most versatile roles in League of Legends. It features AD and AP assassins, AP mages, ADCs,
	  and seldom even bruisers. This lane is also significant because it is in the middle of the Rift. This allows the laner to reach
	  top lane and bot lane fairly quickly giving them a significant amount of range across the map. They can also be instrumental in taking objectives
	  due to the proximity to baron pit and dragon pit. Because of this, being able to control your lane is key in success at mid lane because if you
	  push the lane not only does it allow you to roam or take objectives undetected but it also forces your opponent to stay in lane so they don't
	  miss crucial XP and gold and take unnecessary tower damage. 
	  
	  <br>
	  <br>
	  
	  Mid lane is an important solo lane. <strong>Because both players are likely going to snowball hard due to the champions that are traditionally taken mid lane,
	  skill is very important in shutting down your opponent as well as get yourself ahead.</strong> Mid lane generally needs the least amount of help to win and
	  because of the large amount of escapes on most mid laners as well as the fact that it's the shortest lane in the game, you need to be able to win the
	  lane yourself because junglers most of the time will have a hard time getting successful ganks off.</p>
	  
	  <h3> Know counters for each champion </h3>
	  
	  <p>While it's very true you can play any champion into any matchup and win, counters statistically do give you an advantage in lane. Some champions simply
	  matchup well versus others and that's an important thing to know when going mid lane. However this does not mean you should always pick counters for the
	  enemy laner and blindly pick champions you have no idea how to play, <strong>this advantage only applies if you know the champion well enough to play them
	  at your current elo.</strong> If you pick a champion that you don't know how to play simply for the advantage a counter pick gives you then you will
	  actually give your opponent a large advantage. Personal skill with a champion trumps counters every time, but counters can be a very effective way to easily
	  win a matchup.</p>
	  
	  <h3> Map awareness is key </h3>
	  
	  <p>Map awareness is one of the most important skills in League of Legends and simply improving that can win you a lot of games, but it's particularly important
	  for a mid laner because they have easy access to all areas of the map. This allows them to easily roam and help other lanes win which is one of the best ways
	  to win a game. Map awareness is also key because any wards you put down are completely useless if you don't look at the map and use them. You can also see where
	  the enemy jungler last appeared which allows you to dodge incoming ganks and also allows you to potentially secure an easy kill if you see them enter their jungle
	  and start farming a camp with low health. <strong>This is arguably the single most important skill you can improve</strong> and we advise that you try to watch the
	  map every few seconds to get a good idea of what's happening.</p>
	  
	  <h3> Mid lane tower is the most valuable </h3>
	  
	  <p>Going through mid lane is the fastest way to reach the enemy nexus. This is important because taking the enemy mid tower not only helps you achieve the ultimate
	  goal of taking down the enemy nexus, but it also puts significant pressure on the enemy team because it allows your team access to all of the enemy jungle and can significantly
	  throw off the enemy jungler's plans once they lose their mid tower. This also allows you to roam and affect the game even more by helping other lanes win which as mentioned above,
	  is a great way to get your team ahead and ultimately win the game. Since a lot of midlaners are mostly AP and cannot take a tower very effectively, you should try to coordinate with
	  your jungler to take down the tower and only focus the tower (if you're AP, AD champions can take the tower quickly) if there are no options to roam.</p>
	  
	  
