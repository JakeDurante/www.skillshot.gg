 	  <h1> General ADC Advice </h1>
	  
	  <h3> Introduction to ADC </h3>
	  
	  <p>The ADC is arguably the most iconic role in League of Legends. A good ADC can easily win a game simply because of the sheer amount of damage
	  they deal out late game. This role's main job is in the name, they are the carry for the team. They are characterized by being AD heavy,
	  with ranged auto attacks and low health. Because of the importance of this role in late game they generally lane with a support in the bot
	  lane which can keep them alive from ganks by the enemy jungler, give them extra sustain with heals, and/or catch the enemy ADC or support
	  to secure extra kills in the laning phase.
	  
	  <br>
	  <br>
	  
	  ADCs generally prioritize having more gold than their opponent and CS is a huge part of that. <strong>They need to be proficient in last hitting
	  because this will allow them to slowly gain a lead over their opponent.</strong> This is arguably the most important skill for ADCs considering CS
	  is the most reliable way to get strong, each ADC has slightly different auto attack styles so it may be a good idea to go into a normal game
	  and practice with a new ADC before taking them to ranked. ADCs also need to be careful with their positioning, because of the sheer amount of
	  damage they can do coupled with their low health, they are generally a high priority by the enemy team to catch and kill in a fight. Because of
	  this you need to be sure you position correctly so that your tanks take all the damage and you can melt the enemy team from behind your front line.</p>
	  
	  <h3> Laning phase </h3>
	  
	  <p>Laning phase is one of the most important times for an ADC to do well because getting strong in laning phase means you will catapult to where
	  you need to be faster and you can start to help your team in teamfights. Laning phase for an ADC is the most unique of all laners because most ADCs
	  don't have the ability to out right kill an enemy by themselves due to lack of CC. This is where the support comes in. Some supports such as Alistar,
	  Thresh, or Blitzcrank provide a ton of CC and can pressure enemies to be careful when CSing. This not only throws them off when trying to last it but if
	  they do make a mistake and your support CCs them, you need to be able to follow up and get the kill. Getting an early kill as an ADC is amazing because it
	  can easily snowball into a huge lead later.
	  
	  <br>
	  <br>
	  
	  Some supports are more defensive such as Janna, Soraka, or Zilean. In this case your goal is to not let your enemy snowball and try to coordinate with your jungler
	  for ganks. Keep in mind that your jungler potentially could prioritize your lane less than normal due to the lack of CC from your support so you simply need
	  to focus on not getting killed in lane and last hitting well. If you do this properly you generally will have a bigger advantage than your opponent because
	  you'll have more defense from your support in team fights. This is highly situational however and may not be the case in every game, so keep that in mind.</p>
	  
	  <h3> Mid/Late game </h3>
	  
	  <p>Mid and late game is where the ADC role shines. At this point assuming you're not incredibly behind you'll have a large amount of damage and can start
	  to kill enemies fairly easily and quickly. This is where positioning is the most important because with your large amount of damage comes a large amount of
	  pressure form the enemy team to be caught out of position and killed.<strong> A significant mistake at this stage in the game by the ADC could easily lose the game, but at
	  the same time a great play by the ADC can win the game.</strong> In lower elos it can be more difficult to survive and deal damage to the enemy team because most players will
	  blindly dive the other team and not peel for their carries. This can be frustrating but with enough practice you can kite your enemies and hopefully take a few down with
	  you, allowing the rest of your team to clean up. Another great strategy is to be the one getting the clean up by coming in slightly late to a fight and finishing off all of
	  the low enemies. This works well because the enemy champions that would normally dive you will be too low to do so and you'll be able to do damage nearly untouched.
	  
	  <br>
	  <br>
	  
	  ADCs also need to know when to focus on CSing and taking objectives/team fighting. A lot of common mistakes made by ADCs at all elos is to prioritize farm too much at late
	  stages in the game. As mentioned earlier CS is incredibly important for an ADC and it doesn't become any less important at the later stages of a game but it does present a
	  large risk for you to be away from your team farming, especially so if you are alone in a lane and overextended because the enemy team will try to catch you when you're alone and
	  kill you. So you need to be very careful when farming late game. <strong>In cases where your team decides to do baron or dragon, you should always be there to help as your damage will help
	  secure it much quicker and will minimize the chance the enemy team will be able to stop your team.</strong></p>
	  
	  
