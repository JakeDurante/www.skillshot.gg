 	  <h1> Why Toxicity Loses Games</h1>
	  
 
 	  <h3> What makes us toxic? </h3>
	  
	  <p>It happens to all of us, you get that feeder or teammate who refuses to work with the team. You get that guy is hell-bent on making you and your team lose.
 	  While Riot does their best to stop these people, sometimes they appear in your games and wreak havoc. Most people get angry and rightfully so but when you start
 	  to type your toxic thoughts, not only are you at risk of getting banned but you have pretty much thrown your chances of winning out the window. This is because
	  when we are angry our mindset changes. No longer do we want to beat our opponents, but now we want to beat our teammates. Once you start retaliating (or initiating the toxicity) you start
	  to focus more on a clever comeback or insult and not on winning the game, the solution when this happens depends on the person, some like to mute the toxic teammate and move on,
	  whilst others need to vent their anger out loud (but not to their teammates!)<br>
	  
	  <br>We all get angry at our teammates and that is fine, but don't let them know that they messed up or encourage them to troll in your games. In the case where
	  your teammate is just a normal person who happened to make a mistake or is having a bad game, letting them know that they messed up or <strong>saying passive-aggressive
	  comments such as "why..." only lead to tilt and your inevitable defeat.</strong> In many games, the team that wins is largely determined by who can keep their heads in
	  the game and not tilt their teammates.<br>
	  
	  <br>Anytime you're tempted to get mad at your teammates make sure to ask yourself "What do we as a team gain out of saying this?" While in most cases telling someone
	  that they messed up and not to do it again would seem like it helps you really don't help them change their play at all. The only thing you do in this case is tilt them
	  and lead to more mistakes which brings us to the primary point of this article:</p>
	  
	  
	  <h4><strong> DON'T TRY TO TEACH YOUR TEAMMATES IN-GAME </strong></h4>
	  
	  <p>A lot of people think they can fix their teammates or subconsciously try to teach them how to be better at league when in reality they learn nothing from what
	  you said (unless they read our article on listening to your teammates) and it proceeds to tilt them further. In most cases, simply restrict your chat to shot-calls
	  and positive comments about what your teammates have done such as telling them "good job" when they get a kill or a "nice shot" if they land a good skillshot. Building your
	  teammates' confidence is one of the best ways to keep your team mentally in the game and it does the exact opposite of toxic behavior: it builds their confidence and lends to
	  better play and less mistakes.<br>
	  
	  <br>More importantly, always try to focus on your own play instead of your teammates. It will allow you to realize that you make plenty of mistakes in every game and will help
	  you fix them to become a better player more quickly. There's a reason that people "trapped" in low elo call it "elo hell." It's because they spend so much of their time and energy
	  thinking about how bad their teammates are that they fail to realize that they are making tons of mistakes that could easily be fixed if they just paid attention. Not only does curbing
	  your toxicity benefit your teammates by not having to deal with toxicity from you but it also helps you win more games by not tilting your teammates and it will help you improve much
	  faster. At the end of the day it is a game and plenty of people just play for enjoyment. This may mean they do not play at the best times, I've encountered many people playing in weird settings
	  and at weird times, sometimes you need to just up your own game and carry them to victory.
