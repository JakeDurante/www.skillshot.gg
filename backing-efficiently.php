 	  <h1> When You Should go Back to Base </h1>
	  
	  <h3> Laning Phase</h3>
	  
	  <p>Lane phase is a very tricky part of the game. A few small missteps and it can cause your lane to
	  spiral out of control and even cost you the game. Therefore you need to minimize these mistakes to win
	  more of your games. One of the common mistakes people make is when they go back to the fountain to buy
	  items and heal. They tend to back at improper times costing valuable XP and farm and making them fall
	  behind their opponent. Generally when laning you want to back at these times (in priority order):</p>
	  
	  <ul>
	  <li> After pushing your lane under the enemy tower </li>
	  <li> Try to back only when you can buy something (but be sure not to overstay) </li>
	  <li> After forcing your opponent out of lane (if your low on hp) </li>
	  </ul>
	  
	  <p>If you can back after forcing the wave under the enemy tower then it's unlikely you will lose much
	  XP or gold and it will help you deny your opponent. If you can't do this it's possible your opponent could
	  take your tower or force you to use teleport (if you're a top laner.) If you cannot do that then try to back
	  when you can purchase an item, that way you can come back to lane stronger than before. </p>
	  
	  <h3> Mid-Game </h3>
	  
	  <p> Mid-game it's a little bit easier to determine when to back because it's generally when the rest of your
	  team starts to back or when you're low. But sometimes people even in high elo back at improper times and
	  can really slow down their team because of it. In general you should only back when low or when you can buy an
	  item <strong> at a time your team does not immediately need you.</strong> Some people have the terrible habit of
	  backing for a small item right when their team is about to fight and then get mad when someone gets caught and they
	  die 4v5. <strong> Do not leave your team right before a fight or before an objective is about to be taken.</strong>
	  This is a fairly common way to throw a game and it occurs at all elos.</p>
	  
	  <h3> Late-Game </h3>
	  
	  <p>Generally late-game is the same as mid-game but the stakes are even higher here because one small misstep can cost
	  the entire game. Be sure you are not backing in a place where a ward could be or in a position where the enemies could
	  ambush you because late in the game if you get caught it may lose the game for you and your team. Again make sure not to
	  back right before a fight and late-game there are few items that can really change the game by themselves. In solo queue where
	  communication is much more restricted it's a good idea not to risk a miscommunication by backing for an item at a bad time.</p>
	   
	  
