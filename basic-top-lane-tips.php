 	  <h1> General Top Lane Advice </h1>
	  
	  <h3> What are my responsibilities? </h3>
	  
	  <p>Top lane is an interesting role because it is the most isolated lane in League of Legends. This lane is the most susceptible
	  to ganks since it is just as long as bot lane but you are alone. Because of this you need to be able to ward and keep an eye on
	  the enemy jungler because it is much more difficult to escape a gank alive when top lane than mid or bot lane. Most top laners
	  that are played are tanks and bruisers which means you don't necessarily need to snowball to win (though of course that's always
	  something you should strive for because it never hurts.)
	  
	  <br>
	  <br>
	  
	  Because of the isolated nature of the lane most top laners take teleport so that they can affect plays elsewhere on the map. This allows
	  them to be present for taking dragon/gives them the ability to gank bot and mid lane easily. <strong>Generally the most effective use of teleport
	  comes from either taking dragon or ganking bot lane, especially if your support was able to get a ward behind the enemy team.</strong> The top laner
	  is usually one of the champions responsible for peeling allied carries and can also be very effective at split pushing depending on the
	  champion.</p>
	  
	  <h3> Early Game </h3>
	  
	  <p>As a top laner, your goal in the early game is to deny your opponent as much as possible while gaining as much gold and XP for yourself
	  as you can. The most effective way of doing this is obviously killing your opponent and this can be devastating to your opponent if it happens
	  more than once. Coordination with your jungler is very important because you need to know when ganks will occur. Successful ganks will not only
	  kill your laner/burn their summoner spells, but it may also result in taking the enemy tower or even Rift Herald. In most cases, the mid-game will
	  start once one or both towers fall.</p>
	  
	  <h3> Mid Game/Late Game </h3>
	  
	  <p>As mentioned before the top laner is generally much tankier than most of their team. Because of this your main priority, assuming your carries
	  aren't very behind, is to peel for them. For more information on when you should peel and when you should lock down enemy carries, check out the
	  article on <a href="peeling.php">peeling/diving.</a> Depending on the champion, some top laners are also very effective split pushers. This is 
	  extremely powerful in the top lane assuming you took teleport as a summoner spell, because it allows to provide pressure on the enemy by pushing a
	  side lane and gives you the ability to teleport into a fight should one occur. <strong>Just be careful when you decide to make this play as overextending
	  without vision can give the enemy team a great opportunity to catch you unaware and kill you, leaving your team at a 4v5 disadvantage.</strong>
	  
	  
	  <br>
	  <br>
	  
	  Top lane is a very powerful role because it is so isolated. You can completely take an enemy out of the game if you deny them enough gold/XP and it
	  can easily lead to victory. The key to turning a fed performance into a victory is knowing how to <a href="snowballing-top-lane.php">snowball a game as a top laner.</a>
	  You are generally tasked with keeping carries alive in fights and leveraging your teleport (if you decided to take it) to make plays all over the map.</p>
	  
	  
	  
	  
