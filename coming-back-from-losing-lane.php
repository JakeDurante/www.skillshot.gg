 	  <h1> What to do When Behind in Lane </h1>
	  
	  <h3> Don't fight your laner </h3>
	  
	  <p><strong>When you fall behind in lane, a huge mistake that a lot of people make is to fight your laner.</strong> In most cases, you will lose that fight and further dig
	  yourself into a hole. Once you give up a kill to your laner, just focus on farming and wait for a gank from the jungler. (This
	  does not mean you should pester them into ganking.) You should be mindful of your position in the lane at this point as the
	  second you step foot into your opponent's half of the lane they may try to all-in you in a bid to get even further ahead or worse, the
	  enemy jungler will come back again for a second gank to further snowball the lane. It's better to lose minions and not take the abuse
	  that a fed laner will give you than to try and take those minions and potentially get killed.
	  
	  
	  <br>
	  <br>
	  
	  As mentioned before in the article on <a href="item-builds.php">item builds,</a> when behind, in most cases, you should build
	  defensively. For top laners this may mean a tank item, for mid laners this may mean Zhonya's Hourglass, etc. Building tank does
	  make you do less damage obviously but if you're behind in lane and you build damage it's very likely you won't survive to deal
	  your damage. Plus since your opponent is ahead they likely do more damage than you do.
	  
	  <h3> Patience is key </h3>
	  
	  <p><strong>Sometimes a fed enemy will get cocky and try to extend their lead instead of simply zoning you. This is a great opportunity to
	  come back in the lane if you can bait them into diving you and kill them.</strong> This is even better if you can bait their jungler as well
	  because if you can land a double kill (and it's relatively easy assuming you have cc and flash up) you can easily take back your lane
	  and dominate from there on out. This does not always happen and if it doesn't simply try to farm as much as you can and consider split pushing
	  to catch up in gold to your opponent. Split pushing is very effective in this regard because it can apply significant pressure to the enemy
	  team and will allow you to farm a lot of minions quickly so you can catch up on gold.
	  
	  
	  <br>
	  <br>
	  
	  Being passive and "turtling" is a great way to catch up because it can really frustrate your opponent and force them to make bad plays. Often
	  an enemy jungler can be baited into a bad play by making them wait long periods if you see them trying to set up a gank. It will either lead to
	  them making a horrible play to try to kill you or they will lose significant gold and XP allowing your jungler to come gank without fear of a
	  devastating counter-gank. This strategy is extremely effective for champions with significant 2v1 potential as well, because if you can force
	  the enemy jungler to make a bad play to try to kill you, you can turn the tables and win the lane (this is assuming that you aren't terribly far
	  behind, if you are it's unlikely that this will work.)</p>
	  
	  <h3> Consider roaming if you really can't farm </h3>
	  
	  <p> Even though you're likely weaker than you should be since you're behind in lane, if your opponent is zoning you and you simply cannot
	  cs you should consider ganking another lane. This can get you some much needed gold in the form of potentially a kill and a tower and
	  can dramatically change the matchup in the lane that you ganked. This is a very important concept because it allows you to then come back
	  and potentially take back your lane as well. Regardless, you should never give up when behind in lane because there are always ways to come
	  back and win the lane. There are multiple ways an enemy can misplay and hand the lead back to you even without your jungler ganking for you.</p>
	  
