	  lot of a champion that they have a very low winrate with! Some people just don't know about these websites or don't check them and
	  while that makes sense, this is a great way to improve! If you have a 42% winrate with Vayne and a 65% winrate with Jinx, you should
	  definitely consider playing Jinx more often!</p>
	  
	  <h3> Other stats that are helpful </h3>
	  
	  <p>There is a ton of information on op.gg that can help you improve. You can also see your average amount of cs per game which can
	  help you determine if your csing is good enough. It also tells you your kill participation which is very useful for junglers to make
	  sure that they aren't farming too much and are having an impact on the game. There is a ton of useful information on the site and all
	  of the other similar sites that a lot of people don;t even think about utilizing to help them improve. This is a great way to check how
	  you do in certain areas compared to your peers, maybe you cs better but you're still losing a lot, that might mean you don't win your
