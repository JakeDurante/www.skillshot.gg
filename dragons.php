 	  <h1> An In-Depth Look at Dragons </h1>
	  
	  <h3> How high of a priority are they? </h3>
	  
	  <p> The new dragon system made the dragons much stronger than before. Dragons are now very high priority in early/mid game situations and
	  you should look to take them whenever possible and be there when they spawn. Games are now very difficult to win if your opponent has
	  significant dragon advantage on you. Because of the strength of these dragons
	  junglers that feature an ability to control objectives will have an advantage over the others which allows champions that can solo the dragon easily to be even stronger
	  in games now since they have exceptional objective control. </p>
	  
	  <h3> Infernal Dragon </h3>
	  
	  <p> Infernal dragon is extremely strong when taken by a team that is starting to snowball. This is because teams that are winning will take more offenive build paths,
	  therefore allowing this dragon to give them even more AD/AP than it otherwise would. Infernal Dragon can provide thousands of gold worth of stats and if your team gets a
	  2 or 3 infernal dragon advantage, it's very unlikely that you will lose the game. Because of this, infernal dragon is a high priority at all stages of the game but slightly
	  more so in the later stages.</p>
	  
	  <h3> Ocean Dragon </h3>
	  
	  <p> The Ocean Dragon is the strongest dragon in the early game. If you have a jungler that can solo dragon or get an early opportunity to take this dragon,
	  do so. It provides teams with the highest winrate of any dragons when its taken at 11 minutes or earlier. This dragon does however fall off significantly in
	  the late game and is one of the worst in late game situations, therefore it has high priority in the early game and low priority in the late game. </p>
	  
	  <h3> Mountain Dragon </h3>
	  
	  <p> The mountain dragon is a fairly underrated dragon. It provides your team with the ability to take objectives faster. While unlike the infernal dragon in that the buff
	  doesn't inherently give your team much of an advantage, it provides your team with the tools necessary to get advantage by taking objectives quicker. One or two of these dragons
	  is enough to take Baron extremely quickly and that should be kept in mind when mountain dragons are in play. These are arguably the strongest dragons in the game and should be high
	  priority at all times, as they give your team the ability to comeback in the game or snowball quickly. </p>
	  
	  <h3> Cloud Dragon </h3>
	  
	  <p>The Cloud dragon gives a decent amount of movement speed and does provide some benefit to teams due to the increased ability to catch enemies
	  unaware and also to stop split pushing teams. Cloud dragons simply do not provide enough of an advantage to really warrant taking them but it is worth it
	  when your composition features movement speed dependent champions such as Hecarim. It also should be taken simply to get the next dragon to spawn sooner. Sometimes it may be a good
	  strategy to bait an enemy to take the Cloud dragon while you take another objective.</p>

	  <h3> Elder Dragon </h3>
	  
	  <p>The Elder dragon gives a burn buff to the team that slays it which increases with the number of dragons taken previously. With no dragons the buff
	  is very weak but is still good for denying an enemy team the buff. It also augments the current buffs from previous dragons and is especially good if a
	  team has two or three stacks of a particular buff because it gives a significant bonus to that. The Elder dragon is much harder to kill than previous dragons
	  but is still easier than baron. This should be of very high priority because it will either give you a strong buff or deny the enemy a very strong buff
	  but beware the duration has been reduced significantly from the 5th dragon of the previous system.</p>
	  
