 	  <h1> Snowballing the Game as a Top Laner </h1>
	  
	  <h3> How do I start a "snowball?" </h3>
	  
	  <p>Snowballing the game is something a lot of players struggle with. If you've ever had a game where you got very fed
	  and destoryed your lane opponent but proceeded to lose pretty handily, it's likely that you didn't snowball the game effectively.
	  Kiiling your lane opponent is very good, but it has diminishing returns. Once you've won the lane, you need to focus on
	  helping your teammates win their lanes. To do this, it's a great idea to take your opponent's tower and push the lane if
	  possible but just because your opponent has their tower up should not stop you from going to other lanes.
	  
	  
	  <br>
	  <br>
	  
	  One common and very effective way to snowball the game is the teleport play. If you are able to teleport behind the enemy
	  bot lane and kill them, it puts your bot lane very ahead. At this point you should try to take either the tower or dragon
	  especially if your tower is not being taken (if it does go down, it's not a bad trade as long as you can get the enemy bot
	  lane tower.) You can also start a snowball mid by simply walking down and ganking for them. <strong>Any time one of your ganks are successful,
	  always try to help the laner take the tower or another objective.</strong> Taking other lane's towers with them is what really
	  gets a snowball going and can help you end the game quickly.
	  
	  
	  <br>
	  <br>
	  
	  If you happen to run into a situation where it is very hard to help another lane, focus on zoning your lane opponent and denying them cs
	  until one appears. The more behind your opposing laner is, the easier the game will be in the mid and late game for your team.
	  When ahead, zoning your opponent and putting them slightly further behind should not be the priority. It should simply be something
	  that you do when waiting for an opportunity to appear.
	  
	  <h3> When ahead, apply as much pressure as possible </h3>
	  
	  <p>Assuming you got a large lead from laning phase and were able to deny your laner a significant amount of gold/XP, try to shove top as much
	  as you can while you wait for an opportunity to group or gank another lane. This is especially powerful if you believe you can escape a 2v1
	  between the enemy jungler and top laner because you can apply a ton of pressure and maybe even take an inner tower since it will be very hard for
	  them to stop you. If the enemy jungler does come for you it also opens up an opportunity for your team to exploit his positioning on the map and take
	  other objectives since they know he won't show up and stop them.
	  
	  
	  <br>
	  <br>
	  
	  Split pushing as a fed top laner can be extremely effective because it is hard for the enemy team to stop you. This makes them send 2 or even 3 champions
	  your way to block you and again opens up opportunities for your team elsewhere. If you have teleport and its off cooldown, you can split push and wait for
	  the enemy team to send champions your way then teleport to your team and try to fight the enemy with the man advantage. This is also an effective way of taking
	  baron because if the enemy team tries to stop your team you can teleport in and win the fight. If they try and contest a fail to do so, your team not only gets baron
	  but whatever tower your pushing down as well and the baron buff makes your split pushing much more effective.</p>
	  
