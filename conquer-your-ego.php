 	  <h1>The Biggest Thing Holding You Back<h1> 
	  
	  <h3>Learn to master your ego</h3> 

	  <p>A lot of newer and lower elo players in league of legends have trouble overcoming their ego. It's not an uncommon thing amongst humans,
	  many people have trouble admitting when they have messed up or when they are wrong. This is what elo hell really is. Elo hell in the
	  traditional sense does not exist. The very thought of being stuck in elo hell is what is keeping you from escaping it. If you truly
	  believe that elo hell is real then you have not yet overcome your ego. If you are in a certain division of ranked, that is because
	  you are at that skill level.<br>
	  
	  <br><p>There is nothing wrong with being at a lower skill level on the ranked ladder. In fact itâ€™s a tremendous opportunity to grow as a person,
	  because mastering your ego will help you tremendously in many aspects of your life even outside of league. If you want to rank up, you need to be
	  able to focus on your mistakes. Sure it would have been nice if Leona didn't try to 1v1 the fed Vayne, but if you hadn't made a bad decision earlier,
	  Vayne wouldn't have been fed. The thing that most people struggle with is that it is so easy to blame your teammates for their mistakes. It makes you
	  subconsciously feel good and believe you did not make a mistake, even though in reality you did.</p>
	   
	  <h3>How to spot your mistakes -- not your teammates</h3> 

	  <p>The best way to spot mistakes is to be hard on yourself. There is absolutely no game in which you have played perfectly. The best opportunities for
	  finding mistakes come in deaths, they are easy to think about and realize what led up to the event in which you died. You should be analyzing mistakes
	  both in-game and post-game. During the game make sure you consciously think about what happened every time you see the death screen. Post-game think
	  about what aspects really helped you win/lose and try to repeat them if they are good or stop doing them if they are bad.<br>

	  <br>Though looking at deaths are the easy way of finding mistakes and are one of the easiest ways to improve as a player, there are also decision making
	  mistakes that are made. This is especially true for junglers as their role requires lots of strategy and cognitive effort to succeed, but applies to all
	  of the roles in the game.</p>
	  
	  <h3>Why feeders and AFKers are helping you climb</h3>
	   
	  <p>A common misconception is that feeders and AFKers can keep you stuck in an elo that you don't belong in. This is completely false and is backed up by 
	  statistical evidence. In any given ranked game there are 4 potential people that can go AFK or feed on your team (assuming you don't do so.) However there
	  are 5 potential feeders/AFKers on the enemy team. This in fact actually helps you climb! You have a 46% chance of getting a feeder/AFK as opposed to your
	  opponents' 56% chance. The reason it doesn't feel like your opponents go AFK/feed is because of confirmation bias.<br>

	  <br>"Confirmation bias (or confirmatory bias) is a tendency to search for or interpret information in a way that confirms one's preconceptions, leading to
	  statistical errors." (Wikipedia) In layman's terms this means that we tend to remember the teammates that feed and ruin our games and don't remember those
	  that feed or go AFK on the enemy team. It's also harder to notice an AFK enemy in league and that contributes to this bias as well. With trolls/AFKers/feeders
	  out of the way the only variable left is you! Of course you will get some unwinnable games but most games can be won if you play well. If you consistently
	  play better than your enemies you will consistently win and as a result you will climb.<br>

	  <br>Your ego is the biggest thing holding you back. Players that master their ego and are able to admit their mistakes instead of blaming their teammates climb
	  much faster than those who do not. Once you start to look at your mistakes you'll see many avenues to improve upon and once you start to fix those errors you
	  will start to notice a shift in your play. All of a sudden you will dominate your current elo and start to climb rapidly. As Lee Sin says, "Master yourself,
	  Master the enemy."</p>

