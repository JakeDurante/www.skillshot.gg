 	  <h1> Objectives </h1>
	  
	  <h3> Why objectives are important </h3>
	  
	  <p> Having the knowledge of when to take objectives is one of the most important and undervalued skills in
	  league of legends. This is what separates gold/plat elo from diamond and master elo. To truly become great
	  you have to have complete understanding of objectives. One of the main reasons players are stuck in low elo
	  is because instead of taking towers, barons, or dragons they opt to chase one low health player for the kill.
	  A lot of low elo players respond saying, "kills are just as important, you can't take towers without kills."
	  While this is true, <strong> you should always look to take objectives </strong> because they give you a much
	  more significant advantage over your opponents as opposed to a kill or two. Instead of chasing the low health
	  ADC, take the bot tower and dragon.<br>
	  
	  <br>If you find yourself throwing a lot of games, you should consider your decisions on which objectives to
	  take or pay attention to if you are chasing enemy champions too much. Taking the wrong objective or not taking
	  any when you should be are easy ways to throw. Once you get this decision making process down you will start to
	  rank up quickly because objectives can help you win otherwise unwinnable games. (There are many games where the
	  team with less kills and sometimes gold win the game due to superior objective taking) </p>
	  
	  <p> The situations mentioned below are simply a rule of thumb. <strong>They are not always the correct play</strong> but they are in most
	  cases.</p>
	  
	  <h3> Towers </h3>
	  
	  <p>Towers are very important objectives, the reason they are undervalued is because their benefit is hard to notice.
	  Towers allow your team to have more control over the map and put much more pressure on the opponent. As of patch 6.15,
	  tower firstblood is a massive priority. It gives the standard 350 gold (it was reduced a little in 6.15) as well as 400
	  gold (275 local gold, 25 per member of the team.) This is a huge advantage as the first tower in a game nets you and your team 750 gold.
	  Inners give less, but give <strong> significant </strong> pressure on the enemy team and can allow you to take other objectives easier.
	  Inhibitor turrets are much harder to take down and give little gold, but for obvious reasons are huge priorities.</p>
	  
	  <h3> Dragons </h3>
	  
	  <p>Dragons as of 6.9 are much stronger than before, they can easily win games by themselves because of their buffs. In general,
	  <strong>if there is a successful gank bottom lane early in the game, always take the dragon first. </strong> It gives significant
	  buffs and allows your team to potentially get another dragon before the elder spawns, an additional benefit. The elder dragon is
	  also very good but actually weaker than getting the 5th drag in the previous system because of its short timer and dependency on
	  previous dragon buffs. For more information on dragons and their priority, check out <a href="dragons.php">our article on dragons</a></p>
	  
	  <h3> Baron/Rift Herald </h3>
	  
	  <p>Rift Herald is a fairly strong buff considering it lasts 20 minutes from when it was obtained. It is hard to kill though so
	  you should take it later in the game or in a situation where the top laner is not present. (e.g. teleported bot lane) Herald is not too
	  high of a priority compared to dragons early game due to the fact that it is a one-man buff but it is still good when it's possible to get it.
	  You generally want to give it to a laner who is alone (so not ADCs) because of the damage reduction it gives to lone champions. It also has
	  a lesser effect on champions that are ranged so you generally want to give it to a melee champ, most of the time the top laner should receive the buff. Baron of
	  course is a massive game changer giving a large amount of gold to the whole team and very powerful buffs. This is a dangerous objective however and
	  can make it easy to throw a game if you are caught when taking it. As a general rule of thumb, consider doing baron if the enemy has 2
	  or more players dead or potentially just the jungler. (you have to be able to survive a potential 4v5 fight though while doing baron)
	  However in most cases it more beneficial to go for the enemy inhibitor over baron because it sets you up to be able to take it easily
	  afterwards.</p>
	  
	  <h3> Inhibitors </h3>
	  
	  <p> Inhibitors are very powerful objectives because they open up the enemy nexus to be destroyed and can keep them pinned in their base
	  fighting off super minions. If you combine a downed inhibitor with baron buff, it can be very hard for the enemies to stop and can win
	  most games. These should be of the highest priority in most cases because taking an inhib gives you a great chance at winning the game
	  because it provides immense pressure and can open up other objectives. </p>
	  
	   
	  
	  
	  
