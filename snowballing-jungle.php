 	  <h1> Transitioning a Lead Into Victory (Jungle) </h1>
	  
	  <h3> Take a tower ASAP </h3>
	  
	  <p>As the jungler it is your job to control the game. When you get a lead you should instantly go for a tower. There
	  are several opportunities throughout the course of the early game to do this. You can simply gank and kill the enemy
	  laner likely leaving you an opportunity to take the tower but keep in mind this may take enough time that the enemy jungler
	  will probably <a href="map-control.php">exploit your position on the map</a> and take an objective or gank another lane.
	  Another opportunity to take the enemy tower is to wait for them to back and simply bum rush it with your laner. In lower elos
	  this may anger your laner because they don't understand how important getting the tower is, and they will likely ping you back
	  thinking that you are just trying to steal farm. Ignore them and go for the tower anyways because this will free up your laner
	  to then roam and help take other towers as well as give global gold to your team.
	  
	  <br>
	  <br>
	  
	  <p>It doesn't matter if you are extremely fed if you cannot transition your lead into victory. A great thing to do after you took a tower
	  is to bring that laner with you to another lane and 3 or 4 man gank the and take the next tower. This allows your team to quickly snowball
	  and pick up a huge lead in the early game which can completely decide a game.</p>
	  
	  <h3> Group when ahead </h3>
	  
	  <p>Another great strategy when ahead is to get your team to group up and try to initiate a 5v5 fight. If you can pull it off you'll easily
	  pick up multiple objectives and potentially baron (if it's not too early.) many times though, when you group with a lead the enemy will
	  simply turtle and not allow you to kill them.<strong> In this case do not try to dive them! </strong> Simply rotate to another tower and
	  try to push it down. If you simply cannot take a tower be patient and wait for either a pick or for a neutral objective to come up and try to fight
	  the enemy there. Most of the time people do not like to give up dragons for free and it's a great place to win a big fight that can catapult your
	  team ahead.</p>
	  
	  <h3> Objectives are key </h3>
	  
	  <p><strong>Make sure that you contest for every objective when ahead.</strong> This is because you will very likely get the objective and win any ensuing fights.
	  As stated earlier, enemies hate to just give objectives away for free so they tend to group around neutral objectives and it can be an easy way to
	  catch them unaware, leading you to taking multiple towers/objectives. Every time you grow your lead by taking a tower immediately try to find an
	  opportunity to take another because enemies tend to run to fallen towers to stop creep waves, giving you a chance to rotate to a nearby lane and
	  capitalize on their positioning on the map.
	  
	  Overall, as a jungler make sure that you <strong>always push for objectives.</strong> This mentality is what separates good junglers from great junglers
	  and you will win a lot more games doing this because it allows you to actually use your lead. If you don't take towers you're stalling the game and
	  giving the enemy team a tremendous chance at catching up and potentially winning the game, so always make sure you don;t just hunt more kills and that
	  you try to end the game whenever possible.</p>
	  
	  
	  
	  
