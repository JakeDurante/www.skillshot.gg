 	  <h1> How to Use the Map to Your Advantage </h1>
	  
	  <h3> Exploiting Enemy Position </h3>
	  
	  <p>Most people in lower elo don't think about how to extract information out of the minimap and apply it to their
	  decision making. As you go up the ranks you'll notice that people can derive more information out of their minimap
	  than those in an elo lower than them. One of the key steps to being good at league is to master the minimap. Most
	  players know to look at it and develop map awareness but a lot don't realize all of the information you can acquire
	  and how to turn that to your advantage.</p>
	  
	  <h3> Let's start with the basics </h3>
	  
	  <p>In low elo players generally don't think about where champions are on the map and what that means. They will see a
	  champion but not really process how to exploit their position. For example: let's say that the enemy has 3 players top
	  lane (top laner, mid laner and jungler) and they just killed your top laner. As the midlaner most people wouldn't think
	  about the situation and head top once they see the 1v3 fight top lane. However a smarter play (especially if you're AD or
	  you have a nearby jungler willing to help) would be to push your mid lane and try to take the tower because it's very unlikely
	  that you will be able to save your top tower or get anything from that situation -- in fact, you'll probably die as well and
	  give a second kill to your enemies.
	  <br>
	  <br>
	  Knowing how to exploit your enemies position on the map is crucial to success in league. The scenario described above is generally
	  a very low elo occurrence and stops as soon as high silver but it is very important to those who don't think about it. As you get
	  higher in elo the information you'll need to extract from the minimap will increase. High elo players can see an enemy flash on the map
	  and determine where they came from, what they likely did there, what camps were taken (if they saw the enemy jungler) and have a pretty
	  highly accurate prediction of where they will go. This is exactly why showing on the map as little as possible as a high elo jungler is
	  crucial because just flashing up on the other team's minimap for a second mid lane can get you counter ganked elsewhere. </p>
	  
	  <h3> Always think about where your enemies are or where they could be </h3>
	  
	  <p>Another beneficial strategy is to constantly consider where your enemies could be. A lot of people in low and sometimes even high elos will chase
	  an enemy too far into the fog of war where enemies are waiting. You need to always think where you would be if you were on the enemy team.
	  This thinking will save you many, many deaths and help you win a lot more games. This is also crucial for situations involving baron. If you
	  don't see any enemies on the map for a few seconds and you know they didn't just go back to base, it's fairly likely they are doing dragon
	  or baron. If you're able to quickly recognize these situations you can stop the enemy team while they do the objective, gaining your team a
	  massive advantage in the ensuing fight and potentially gaining the objective as well which can completely swing a game around.</p>
	  
