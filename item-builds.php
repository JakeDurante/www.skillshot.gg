 	  <h1> Proper Item Builds </h1>
	  
	  <h3> Never have a static item build </h3>
	  
	  <p>One common issue that lower elo players have is their item builds. A lot of people tend to follow one item build
	  and assume that it works in every situation which is false. <strong> Item builds should always change between games.
	  </strong> This is because a full damage build on a bruiser is not a good decision if your team is down 5,000 gold.
	  When your team is behind in most cases you should be building defensive items because they will allow you to survive
	  enemy tower dives and survive their burst easier.
	  
	  <br>
	  <br>
	  
	  Even when ahead, you should never follow the same build every time. Defensive items should be chosen based on whether the
	  enemy team is AD or AP heavy and the amount of gold the enemy carries have should also be taken into consideration. This
	  way you don't build a ton of armor into a nearly full AP team or vice versa. Sometimes building utility items such as 
	  Zhonya's Hourglass or Quick Silver Sash are necessary to survive as a carry and are priority items to build. When ahead you
	  should build more damage than usual but still consider the amount of burst that enemy teams have and be sure you can survive it.</p>
	  
	  <h3> Evolve your build based on the situation </h3>
	  
	  Builds also evolve based on the team comp your team has and your role in it. For example a champion that is going to dive the enemy
	  team and try to CC them should be tanky because if they built damage-heavy they can easily be kited and burst down which stops them from getting
	  their CC off. In situations where your team has a fed carry, sometimes building items that allow you to peel easier are good to buy. Items
	  such as Rylai's Crystal Scepter are great for slowing down assassins or the aforementioned CC-heavy champions.
	  
	  <br>
	  <br>
	  
	  <strong>Always consider your role in the team, whether you're a tank, bruiser, or carry and the situations of the game.</strong> Sometimes you'll need to peel,
	  sometimes you'll need to carry the game and your builds should reflect that. Never follow a static build as every game is different. Some champions have core
	  items and that is fine to build in most games but you should never have more than the same item or two that is built in nearly every game. Once you master
	  building the correct items for certain situations, you'll notice you start climbing quickly as this isn't truly mastered until very high elo. Even the best players
	  build incorrectly from time to time so always keep your build at the forefront of your mind.</p>
	  
	  <h3> Improper builds cost games </h3>
	  
	  <p>There are numerous times where teams have lost easily winnable games simply due to their bad item builds. Knowing when to build each item is something that will largely
	  come with experience, but as stated above there are some basic guidelines by which to consider your decisions by. You need to always be aware of what enemies are building
	  and consider how that will affect your build. For example, if an enemy tank has 2 or even 3 armor items, you should probably get some armor pen if you're AD. Conversely, if
	  you're AP you probably should not buy a void staff as that will have little effect and you will have wasted a significant amount of money. This is an extreme example and in
	  most situations it may be harder to decide, but your item build can easily by the difference between winning and losing, so always keep that in the forefront of your mind when
	  playing a game of League of Legends.</p> 
	  
